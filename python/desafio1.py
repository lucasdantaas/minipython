# Crie um algoritmo que leia um numero e mostre o doblo , triplo e raiz quadra!

x = int(input('digiti um numero: '))
dobro = x * 2
triplo = x * 3
raiz = x ** (1/2)
print('o dobro de {} é {}'.format(x, dobro))
print('o triplo de {} é {}'.format(x, triplo))
print('a raiz quadrada de {} é {}'.format(x, raiz)) 
